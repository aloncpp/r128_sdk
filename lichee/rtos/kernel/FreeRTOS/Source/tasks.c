#if ( INCLUDE_vTaskSuspend == 1 )

	void vTaskSuspend( TaskHandle_t xTaskToSuspend )
	{
	TCB_t *pxTCB;
	uint32_t cpu_sr;
	int processor_id = cur_cpu_id();

		taskENTER_CRITICAL(cpu_sr);
		{
			/* If null is passed in here then it is the running task that is
			being suspended. */
			pxTCB = prvGetTCBFromHandle( xTaskToSuspend );
			if(pxTCB == pxCurrentTCB[!processor_id])
			{
				taskEXIT_CRITICAL(cpu_sr);
				/* should never suspend another cores running thread. */
				goto RETURN;
			}

			traceTASK_SUSPEND( pxTCB );

			/* Remove task from the ready/delayed list and place in the
			suspended list. */
			if( uxListRemove( &( pxTCB->xStateListItem ) ) == ( UBaseType_t ) 0 )
			{
				taskRESET_READY_PRIORITY(pxTCB, pxTCB->uxPriority, 1);
			}
			else
			{
				mtCOVERAGE_TEST_MARKER();
			}

			/* Is the task waiting on an event also? */
			if( listLIST_ITEM_CONTAINER( &( pxTCB->xEventListItem ) ) != NULL )
			{
				( void ) uxListRemove( &( pxTCB->xEventListItem ) );
			}
			else
			{
				mtCOVERAGE_TEST_MARKER();
			}

			vListInsertEnd( &xSuspendedTaskList, &( pxTCB->xStateListItem ) );

			#if( configUSE_TASK_NOTIFICATIONS == 1 )
			{
				if( pxTCB->ucNotifyState == taskWAITING_NOTIFICATION )
				{
					/* The task was blocked to wait for a notification, but is
					now suspended, so no notification was received. */
					pxTCB->ucNotifyState = taskNOT_WAITING_NOTIFICATION;
				}
			}
			#endif
		}

		if( xSchedulerRunning[pxTCB->bind_cpu] != pdFALSE )
		{
			/* Reset the next expected unblock time in case it referred to the
			task that is now in the Suspended state. */
			{
				prvResetNextTaskUnblockTime();
			}
		}
		else
		{
			mtCOVERAGE_TEST_MARKER();
		}

		if( pxTCB == pxCurrentTCB[processor_id] )
		{
			if( xSchedulerRunning[processor_id] != pdFALSE )
			{
				/* The current task has just been suspended. */
				taskEXIT_CRITICAL(cpu_sr);
				configASSERT( uxSchedulerSuspended[processor_id] == 0 );
				portYIELD_WITHIN_API();
				taskENTER_CRITICAL(cpu_sr);
			}
			else
			{
				/* The scheduler is not running, but the task that was pointed
				to by pxCurrentTCB[processor_id] has just been suspended and pxCurrentTCB[processor_id]
				must be adjusted to point to a different task. */
				if( listCURRENT_LIST_LENGTH( &xSuspendedTaskList ) == uxCurrentNumberOfTasks ) /*lint !e931 Right has no side effect, just volatile. */
				{
					/* No other tasks are ready, so set pxCurrentTCB[processor_id] back to
					NULL so when the next task is created pxCurrentTCB[processor_id] will
					be set to point to it no matter what its relative priority
					is. */
					pxCurrentTCB[processor_id] = NULL;
				}
				else
				{
					taskEXIT_CRITICAL(cpu_sr);
					vTaskSwitchContext();
					taskENTER_CRITICAL(cpu_sr);
				}
			}
		}
		else
		{
			mtCOVERAGE_TEST_MARKER();
		}
		taskEXIT_CRITICAL(cpu_sr);
RETURN:
		return;
	}

#endif /* INCLUDE_vTaskSuspend */
