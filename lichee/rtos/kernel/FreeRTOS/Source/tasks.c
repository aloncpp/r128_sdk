static void prvCheckTasksWaitingTermination( void )
{
    uint32_t cpu_sr;	
    int processor_id = cur_cpu_id();

	/** THIS FUNCTION IS CALLED FROM THE RTOS IDLE TASK **/

	#if ( INCLUDE_vTaskDelete == 1 )
	{
		TCB_t *pxTCB;

		/* uxDeletedTasksWaitingCleanUp[processor_id] is used to prevent taskENTER_CRITICAL()
		being called too often in the idle task. */
		while( uxDeletedTasksWaitingCleanUp[processor_id] > ( UBaseType_t ) 0U )
		{
			taskENTER_CRITICAL(cpu_sr);
			{
				pxTCB = listGET_OWNER_OF_HEAD_ENTRY( ( &xTasksWaitingTermination[processor_id] ) ); /*lint !e9079 void * is used as this macro is used with timers and co-routines too.  Alignment is known to be fine as the type of the pointer stored and retrieved is the same. */
				( void ) uxListRemove( &( pxTCB->xStateListItem ) );
				--uxCurrentNumberOfTasks;
				--uxDeletedTasksWaitingCleanUp[processor_id];
			}

			prvDeleteTCB( pxTCB );
			taskEXIT_CRITICAL(cpu_sr);
		}
	}
	#endif /* INCLUDE_vTaskDelete */
}
