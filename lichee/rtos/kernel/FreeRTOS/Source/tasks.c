static void prvAddNewTaskToReadyList( TCB_t *pxNewTCB )
{
	uint32_t cpu_sr;
	/* Ensure interrupts don't access the task lists while the lists are being
	updated. */
	int processor_id = cur_cpu_id();
	taskENTER_CRITICAL(cpu_sr);
	{
		uxCurrentNumberOfTasks ++;
		if( pxCurrentTCB[pxNewTCB->bind_cpu] == NULL )
		{
			/* There are no other tasks, or all the other tasks are in
			the suspended state - make this the current task. */
			pxCurrentTCB[pxNewTCB->bind_cpu] = pxNewTCB;

			if( uxCurrentNumberOfTasks == ( UBaseType_t ) 1 )
			{
				/* This is the first task to be created so do the preliminary
				initialisation required.  We will not recover if this call
				fails, but we will report the failure. */
				prvInitialiseTaskLists();
			}
			else
			{
				mtCOVERAGE_TEST_MARKER();
			}
		}
		else
		{
			/* If the scheduler is not already running, make this task the
			current task if it is the highest priority task to be created
			so far. */
			if( xSchedulerRunning[pxNewTCB->bind_cpu] == pdFALSE )
			{
				if( pxCurrentTCB[pxNewTCB->bind_cpu]->uxPriority <= pxNewTCB->uxPriority )
				{
					pxCurrentTCB[pxNewTCB->bind_cpu] = pxNewTCB;
				}
				else
				{
					mtCOVERAGE_TEST_MARKER();
				}
			}
			else
			{
				mtCOVERAGE_TEST_MARKER();
			}
		}

		uxTaskNumber++;

		#if ( configUSE_TRACE_FACILITY == 1 )
		{
			/* Add a counter into the TCB for tracing only. */
			pxNewTCB->uxTCBNumber = uxTaskNumber;
		}
		#endif /* configUSE_TRACE_FACILITY */
		traceTASK_CREATE( pxNewTCB );

		prvAddTaskToReadyList( pxNewTCB, 1);

		portSETUP_TCB( pxNewTCB );
	}
	taskEXIT_CRITICAL(cpu_sr);

	if( xSchedulerRunning[pxNewTCB->bind_cpu] != pdFALSE )
	{
		/* If the created task is of a higher priority than the current task
		then it should run now. */
		if( pxCurrentTCB[pxNewTCB->bind_cpu]->uxPriority < pxNewTCB->uxPriority )
		{
			/* disable this premeept for in critical region */
			if(processor_id == pxNewTCB->bind_cpu)
			{
				taskYIELD_IF_USING_PREEMPTION();
			}
			else
			{
				/*xIsrYieldPending[pxNewTCB->bind_cpu] = pdTRUE;*/
				send_ipi_interrupt(IPI_SCHEDU, 1 << pxNewTCB->bind_cpu, 0, 1);
			}
		}
		else
		{
			mtCOVERAGE_TEST_MARKER();
		}
	}
	else
	{
		mtCOVERAGE_TEST_MARKER();
	}
}
