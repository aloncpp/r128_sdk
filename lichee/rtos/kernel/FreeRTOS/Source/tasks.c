BaseType_t xTaskPreemptable(void)
{
    	uint32_t cpu_sr;
	int32_t processor_id = cur_cpu_id();
	UBaseType_t uxTopPriority;
	BaseType_t ret = pdFALSE;
	
	/* Find the highest priority list that contains ready tasks. */	
	taskENTER_CRITICAL(cpu_sr);
	portGET_HIGHEST_PRIORITY( uxTopPriority, uxTopReadyPriority[processor_id] );
	if(!listCURRENT_LIST_LENGTH( &( pxReadyTasksLists[processor_id][ uxTopPriority ] ) ) > 0)
	{
		SMP_DBG("fatal error.toppri %d, tasknum %d.\n", uxTopPriority,  listCURRENT_LIST_LENGTH( &( pxReadyTasksLists[processor_id][ uxTopPriority ] )));
		soft_break();
	}
	configASSERT( listCURRENT_LIST_LENGTH( &( pxReadyTasksLists[processor_id][ uxTopPriority ] ) ) > 0 );
	
	/*  add preempt point */
	if(uxTopPriority > pxCurrentTCB[processor_id]->uxPriority || xYieldPending[processor_id] != pdFALSE )
	{
		ret = pdTRUE;	
	}
	taskEXIT_CRITICAL(cpu_sr);
	
	return ret;
}

BaseType_t xTaskResumeAll( void )
{
TCB_t *pxTCB = NULL;
BaseType_t xAlreadyYielded = pdFALSE;
uint32_t cpu_sr;
	int processor_id = cur_cpu_id();

	/* If uxSchedulerSuspended[processor_id] is zero then this function does not match a
	previous call to vTaskSuspendAll(). */
	configASSERT( uxSchedulerSuspended[processor_id] );

	/* It is possible that an ISR caused a task to be removed from an event
	list while the scheduler was suspended.  If this was the case then the
	removed task will have been added to the xPendingReadyList[processor_id].  Once the
	scheduler has been resumed it is safe to move all the pending ready
	tasks from this list into their appropriate ready list. */
  	/*there would be no share resource with another cpu because of pendingreadlist and pendingtick mechanism was disabled. 
	 * so just disable local interrupt
	 */
	__vTaskResumeAll();

	portDISABLE_INTERRUPTS(cpu_sr);
	{
		/*suspend +1, Critical +1, upline -1, so should be 1 */
		if( uxSchedulerSuspended[processor_id] == pdFALSE)
		{
		}
		else
		{
			mtCOVERAGE_TEST_MARKER();
		}
	}
	portENABLE_INTERRUPTS(cpu_sr);

	if( xSchedulerRunning[processor_id] != pdFALSE )
	{
		UBaseType_t uxTopPriority;

		/* Find the highest priority list that contains ready tasks. */	
		taskENTER_CRITICAL(cpu_sr);
		portGET_HIGHEST_PRIORITY( uxTopPriority, uxTopReadyPriority[processor_id] );
		if(!listCURRENT_LIST_LENGTH( &( pxReadyTasksLists[processor_id][ uxTopPriority ] ) ) > 0)
		{
			SMP_DBG("fatal error.toppri %d, tasknum %d.\n", uxTopPriority,  listCURRENT_LIST_LENGTH( &( pxReadyTasksLists[processor_id][ uxTopPriority ] )));
			soft_break();
		}
		configASSERT( listCURRENT_LIST_LENGTH( &( pxReadyTasksLists[processor_id][ uxTopPriority ] ) ) > 0 );

		/*  add preempt point */
		if(uxTopPriority > pxCurrentTCB[processor_id]->uxPriority || xYieldPending[processor_id] != pdFALSE )
		{
			taskEXIT_CRITICAL(cpu_sr);

			portYIELD_WITHIN_API();

			taskENTER_CRITICAL(cpu_sr);
			xAlreadyYielded = pdTRUE;
		}
		taskEXIT_CRITICAL(cpu_sr);
	}

	return xAlreadyYielded;
}
