/*
 * Place the task represented by pxTCB into the appropriate ready list for
 * the task.  It is inserted at the end of the list.
 */
#define prvAddTaskToReadyList( pxTCB, lock)												\
do{																	\
	uint32_t cpu_sr;														\
    																	\
	if(lock)															\
    	{ \
		taskENTER_CRITICAL(cpu_sr);												\
    	} \
	traceMOVED_TASK_TO_READY_STATE( pxTCB );											\
	taskRECORD_READY_PRIORITY( ( pxTCB )->uxPriority, pxTCB );									\
	vListInsertEnd( &( pxReadyTasksLists[(pxTCB)->bind_cpu][ ( pxTCB )->uxPriority ] ), &( ( pxTCB )->xStateListItem ) ); 		\
	tracePOST_MOVED_TASK_TO_READY_STATE( pxTCB )											\
	configASSERT( listCURRENT_LIST_LENGTH( &( pxReadyTasksLists[( pxTCB )->bind_cpu][ ( pxTCB )->uxPriority ] ) ) > 0 ) ;		\
    	if(lock)															\
    	{ \
		taskEXIT_CRITICAL(cpu_sr);												\
    	} \
}while(0)
