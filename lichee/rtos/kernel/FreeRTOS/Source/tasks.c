BaseType_t xTaskIncrementTick( TickType_t dticks )
{
TCB_t * pxTCB;
TickType_t xItemValue;
BaseType_t xSwitchRequired = pdFALSE;
uint32_t spin_sr,cpu_sr;
int processor_id = cur_cpu_id();

	/* Called by the portable layer each time a tick interrupt occurs.
	Increments the tick then checks to see if the new tick value will cause any
	tasks to be unblocked. */
	traceTASK_INCREMENT_TICK( xTickCount[processor_id] );
	/*if( uxSchedulerSuspended[processor_id] == ( UBaseType_t ) pdFALSE )*/

	taskENTER_CRITICAL(cpu_sr);
	if(pdTRUE)
	{
		/* Minor optimisation.  The tick count cannot change in this
		block. */
		TickType_t xConstTickCount = xTickCount[processor_id] + dticks;
		TickType_t OldTickCount = xTickCount[processor_id];

		/* Increment the RTOS tick, switching the delayed and overflowed
		delayed lists if it wraps to 0. */
#ifdef configKERNEL_SUPPORT_SMP		
		xTickCount[!processor_id] = xTickCount[processor_id] = xConstTickCount;
#else
		xTickCount[0] = xConstTickCount;
#endif

		xConstTickCount = xTickCount[processor_id];

		if( xConstTickCount < OldTickCount ) /*lint !e774 'if' does not always evaluate to false as it is looking for an overflow. */
		{
			taskSWITCH_DELAYED_LISTS();
		}
		else
		{
			mtCOVERAGE_TEST_MARKER();
		}

		/* See if this tick has made a timeout expire.  Tasks are stored in
		the	queue in the order of their wake time - meaning once one task
		has been found whose block time has not expired there is no need to
		look any further down the list. */
		if( xConstTickCount >= xNextTaskUnblockTime )
		{
			for( ;; )
			{
				if( listLIST_IS_EMPTY( pxDelayedTaskList ) != pdFALSE )
				{
					/* The delayed list is empty.  Set xNextTaskUnblockTime
					to the maximum possible value so it is extremely
					unlikely that the
					if( xTickCount[processor_id] >= xNextTaskUnblockTime ) test will pass
					next time through. */
					xNextTaskUnblockTime = portMAX_DELAY; /*lint !e961 MISRA exception as the casts are only redundant for some ports. */
					break;
				}
				else
				{
					/* The delayed list is not empty, get the value of the
					item at the head of the delayed list.  This is the time
					at which the task at the head of the delayed list must
					be removed from the Blocked state. */
					pxTCB = listGET_OWNER_OF_HEAD_ENTRY( pxDelayedTaskList ); /*lint !e9079 void * is used as this macro is used with timers and co-routines too.  Alignment is known to be fine as the type of the pointer stored and retrieved is the same. */
					xItemValue = listGET_LIST_ITEM_VALUE( &( pxTCB->xStateListItem ) );

					if( xConstTickCount < xItemValue )
					{
						/* It is not time to unblock this item yet, but the
						item value is the time at which the task at the head
						of the blocked list must be removed from the Blocked
						state -	so record the item value in
						xNextTaskUnblockTime. */
						xNextTaskUnblockTime = xItemValue;
						break; /*lint !e9011 Code structure here is deedmed easier to understand with multiple breaks. */
					}
					else
					{
						mtCOVERAGE_TEST_MARKER();
					}

					/* It is time to remove the item from the Blocked state. */
					( void ) uxListRemove( &( pxTCB->xStateListItem ) );

					/* Is the task waiting on an event also?  If so remove
					it from the event list. */
					if( listLIST_ITEM_CONTAINER( &( pxTCB->xEventListItem ) ) != NULL )
					{
						( void ) uxListRemove( &( pxTCB->xEventListItem ) );
					}
					else
					{
						mtCOVERAGE_TEST_MARKER();
					}

					/* Place the unblocked task into the appropriate ready
					list. */
					prvAddTaskToReadyList( pxTCB, 1);

					prvResetNextTaskUnblockTime();
					/* A task being unblocked cannot cause an immediate
					context switch if preemption is turned off. */
					#if (  configUSE_PREEMPTION == 1 )
					{
						/* Preemption is on, but a context switch should
						only be performed if the unblocked task has a
						priority that is equal to or higher than the
						currently executing task. */
						if(( pxTCB->uxPriority >= pxCurrentTCB[pxTCB->bind_cpu]->uxPriority ))
						{
							if(pxTCB->bind_cpu == processor_id)
							{
								//switch at once on the same core.	
								xSwitchRequired = pdTRUE;
							}
							else
							{
								//switch in next stage of core1.  	
								/*xIsrYieldPending[pxTCB->bind_cpu] = pdTRUE;*/
								send_ipi_interrupt(IPI_SCHEDU, 1 << pxTCB->bind_cpu, 0, 1);
							}
						}
						else
						{
							mtCOVERAGE_TEST_MARKER();
						}
					}
					#endif /* configUSE_PREEMPTION */
				}
			}
		}

		/* Tasks of equal priority to the currently running task will share
		processing time (time slice) if preemption is on, and the application
		writer has not explicitly turned time slicing off. */
		#if ( ( configUSE_PREEMPTION == 1 ) && ( configUSE_TIME_SLICING == 1 ) )
		{
			if( listCURRENT_LIST_LENGTH( &( pxReadyTasksLists[processor_id][ pxCurrentTCB[processor_id]->uxPriority ] ) ) > ( UBaseType_t ) 1 )
			{
				xSwitchRequired = pdTRUE;
			}
			else
			{
				mtCOVERAGE_TEST_MARKER();
			}
		}
		#endif /* ( ( configUSE_PREEMPTION == 1 ) && ( configUSE_TIME_SLICING == 1 ) ) */

		#if (  configUSE_PREEMPTION == 1 )
		if (xIsrYieldPending[processor_id] == pdTRUE)
		{
			xSwitchRequired = pdTRUE;
			xIsrYieldPending[processor_id] = pdFALSE;
		}
		#endif

		#if ( configUSE_TICK_HOOK == 1 )
		{
			/* Guard against the tick hook being called when the pended tick
			count is being unwound (when the scheduler is being unlocked). */
			if( uxPendedTicks[processor_id] == ( UBaseType_t ) 0U )
			{
				vApplicationTickHook();
			}
			else
			{
				mtCOVERAGE_TEST_MARKER();
			}
		}
		#endif /* configUSE_TICK_HOOK */
	}
	else
	{
		/*  not allowed use the pendtick mechanism. */  	
		soft_break();
		++uxPendedTicks[processor_id];

		/* The tick hook gets called at regular intervals, even if the
		scheduler is locked. */
		#if ( configUSE_TICK_HOOK == 1 )
		{
			vApplicationTickHook();
		}
		#endif
	}

	#if ( configUSE_PREEMPTION == 1 )
	{
		if( xYieldPending[processor_id] != pdFALSE )
		{
			xSwitchRequired = pdTRUE;
		}
		else
		{
			mtCOVERAGE_TEST_MARKER();
		}
	}
	#endif /* configUSE_PREEMPTION */

	if(listLIST_IS_EMPTY(&MigrateTaskList) == pdFALSE)
	{
		TCB_t *pxMigrateTCB;    	
		pxMigrateTCB = listGET_OWNER_OF_HEAD_ENTRY( &MigrateTaskList );

		if(pxMigrateTCB != pxCurrentTCB[!pxMigrateTCB->bind_cpu])
		{
			( void ) uxListRemove( &( pxMigrateTCB->xStateListItem ) );

			/* Is the task waiting on an event also?  If so remove
			it from the event list. */
			if( listLIST_ITEM_CONTAINER( &( pxMigrateTCB->xEventListItem ) ) != NULL )
			{
				( void ) uxListRemove( &( pxMigrateTCB->xEventListItem ) );
			}

			/* Place the unblocked task into the appropriate ready
			list. */
			prvAddTaskToReadyList( pxMigrateTCB, 0);
			if(( pxMigrateTCB->uxPriority >= pxCurrentTCB[pxMigrateTCB->bind_cpu]->uxPriority ))
			{
				if(pxMigrateTCB->bind_cpu == processor_id)
				{
					//switch at once on the same core.	
					xSwitchRequired = pdTRUE;
				}
				else
				{
					send_ipi_interrupt(IPI_SCHEDU, 1 << pxMigrateTCB->bind_cpu, 0, 1);
				}
			}
		}
	}

	taskEXIT_CRITICAL(cpu_sr);
	// Cant Be protected By Critical region becasue scheduler Could be Affected By taskXXXX_Critical()
	if((xSwitchRequired == pdTRUE) && uxSchedulerSuspended[processor_id] != ( UBaseType_t ) pdFALSE )
	{
		//not allowd preempt for performance.
		xSwitchRequired = xYieldPending[processor_id] = pdFALSE;
	} 

	return xSwitchRequired;
}
