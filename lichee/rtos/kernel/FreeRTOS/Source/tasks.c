void xTaskYieldToCore(int coreid)
{
	int in_cpus_lock(void);
	TCB_t *pxTCB;
	uint32_t cpu_sr;

    	int processor_id = cur_cpu_id();

    	if(xSchedulerRunning[processor_id] ==  pdFALSE) return;
    	if(xSchedulerRunning[coreid] ==  pdFALSE) return;

	configASSERT( uxSchedulerSuspended[processor_id] == pdFALSE );

	pxTCB = prvGetTCBFromHandle(NULL);

	if(in_cpus_lock())
	{
		SMP_DBG("error, yield holding the critical lock.\n");
		return;
	}

	if(coreid >= configNR_CPUS)
	{
		SMP_DBG("parmeter wrong.\n");
		return;
	}

	if(processor_id == coreid)
	{
		return;
	}

	taskENTER_CRITICAL(cpu_sr);
	/* Remove task from the ready list. */
	if( uxListRemove( &( pxTCB->xStateListItem ) ) == ( UBaseType_t ) 0 )
	{
		taskRESET_READY_PRIORITY(pxTCB, pxTCB->uxPriority, 1);
	}

	if( listLIST_ITEM_CONTAINER( &( pxTCB->xEventListItem ) ) != NULL )
	{
		taskEXIT_CRITICAL(cpu_sr);
		SMP_DBG("logic is wrong.\n"); 
		soft_break();
		return;
	}

	pxTCB->bind_cpu = coreid;

	vListInsertEnd( &MigrateTaskList, &( ( pxTCB )->xStateListItem ) );

	taskEXIT_CRITICAL(cpu_sr);

	portYIELD_WITHIN_API();

	return;
}
