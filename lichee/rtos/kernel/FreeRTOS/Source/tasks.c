BaseType_t xTaskRemoveFromEventList( const List_t * const pxEventList )
{
TCB_t *pxUnblockedTCB;
BaseType_t xReturn;
uint32_t cpu_sr;

	/* THIS FUNCTION MUST BE CALLED FROM A CRITICAL SECTION.  It can also be
	called from a critical section within an ISR. */

	/* The event list is sorted in priority order, so the first in the list can
	be removed as it is known to be the highest priority.  Remove the TCB from
	the delayed list, and add it to the ready list.

	If an event is for a queue that is locked then this function will never
	get called - the lock count on the queue will get modified instead.  This
	means exclusive access to the event list is guaranteed here.

	This function assumes that a check has already been made to ensure that
	pxEventList is not empty. */
        taskENTER_CRITICAL(cpu_sr);
	pxUnblockedTCB = listGET_OWNER_OF_HEAD_ENTRY( pxEventList ); /*lint !e9079 void * is used as this macro is used with timers and co-routines too.  Alignment is known to be fine as the type of the pointer stored and retrieved is the same. */
	configASSERT( pxUnblockedTCB );
	( void ) uxListRemove( &( pxUnblockedTCB->xEventListItem ) );

	if( uxSchedulerSuspended[pxUnblockedTCB->bind_cpu] == ( UBaseType_t ) pdFALSE )
	{
		( void ) uxListRemove( &( pxUnblockedTCB->xStateListItem ) );
		prvAddTaskToReadyList( pxUnblockedTCB, 1);

		#if( configUSE_TICKLESS_IDLE != 0 )
		{
			/* If a task is blocked on a kernel object then xNextTaskUnblockTime
			might be set to the blocked task's time out time.  If the task is
			unblocked for a reason other than a timeout xNextTaskUnblockTime is
			normally left unchanged, because it is automatically reset to a new
			value when the tick count equals xNextTaskUnblockTime.  However if
			tickless idling is used it might be more important to enter sleep mode
			at the earliest possible time - so reset xNextTaskUnblockTime here to
			ensure it is updated at the earliest possible time. */
			prvResetNextTaskUnblockTime();
		}
		#endif
	}
	else
	{
		/* The delayed and ready lists cannot be accessed, so hold this task
		pending until the scheduler is resumed. */
		//vListInsertEnd( &( xPendingReadyList[pxUnblockedTCB->bind_cpu] ), &( pxUnblockedTCB->xEventListItem ) );
		( void ) uxListRemove( &( pxUnblockedTCB->xStateListItem ) );
		prvAddTaskToReadyList( pxUnblockedTCB, 1);
	}

	if( pxUnblockedTCB->uxPriority > pxCurrentTCB[pxUnblockedTCB->bind_cpu]->uxPriority )
	{
		int processor_id = cur_cpu_id();
		/* Return true if the task removed from the event list has a higher
		priority than the calling task.  This allows the calling task to know if
		it should force a context switch now. */
		xReturn = pdTRUE;

		/* Mark that a yield is pending in case the user is not using the
		"xHigherPriorityTaskWoken" parameter to an ISR safe FreeRTOS function. */
		xYieldPending[pxUnblockedTCB->bind_cpu] = pdTRUE;
		if(pxUnblockedTCB->bind_cpu != processor_id)
		{
			xYieldPending[pxUnblockedTCB->bind_cpu] = pdFALSE;
			send_ipi_interrupt(IPI_SCHEDU, 1 << pxUnblockedTCB->bind_cpu, 0, 1);
			xReturn = pdFALSE;
		}
	}
	else
	{
		xReturn = pdFALSE;
	}

	taskEXIT_CRITICAL(cpu_sr);
	return xReturn;
}
