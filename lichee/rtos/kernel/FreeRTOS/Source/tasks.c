// under the kernel big lock ,so need not to be protected.
static void prvInitialiseTaskLists( void )
{
	UBaseType_t uxPriority;
	uint32_t spin_sr;

	int nr_cpus = configNR_CPUS, iter_cores = 0;

	for(iter_cores = 0; iter_cores < nr_cpus; iter_cores ++)
	{
		vListInitialise( &xPendingReadyList[iter_cores] );
		for( uxPriority = ( UBaseType_t ) 0U; uxPriority < ( UBaseType_t ) configMAX_PRIORITIES; uxPriority++ )
		{
			vListInitialise( &( pxReadyTasksLists[iter_cores][ uxPriority ] ) );
		}
	}

	vListInitialise( &xDelayedTaskList1 );
	vListInitialise( &xDelayedTaskList2 );
	vListInitialise( &MigrateTaskList );

	#if ( INCLUDE_vTaskDelete == 1 )
	{
		for(iter_cores = 0; iter_cores < nr_cpus; iter_cores ++)
			vListInitialise( &xTasksWaitingTermination[iter_cores] );
	}
	#endif /* INCLUDE_vTaskDelete */

	#if ( INCLUDE_vTaskSuspend == 1 )
	{
		vListInitialise( &xSuspendedTaskList );
	}
	#endif /* INCLUDE_vTaskSuspend */

	/* Start with pxDelayedTaskList[coreid] using list1 and the pxOverflowDelayedTaskList[coreid]
	using list2. */
	pxDelayedTaskList = &xDelayedTaskList1;
	pxOverflowDelayedTaskList = &xDelayedTaskList2;
}
