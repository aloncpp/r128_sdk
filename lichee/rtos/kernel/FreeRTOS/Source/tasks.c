void vTaskRemoveFromUnorderedEventList( ListItem_t * pxEventListItem, const TickType_t xItemValue )
{
TCB_t *pxUnblockedTCB;
uint32_t cpu_sr;
int processor_id = cur_cpu_id();

	/* THIS FUNCTION MUST BE CALLED WITH THE SCHEDULER SUSPENDED.  It is used by
	the event flags implementation. */
	/* Store the new item value in the event list. */

	taskENTER_CRITICAL(cpu_sr);
	listSET_LIST_ITEM_VALUE( pxEventListItem, xItemValue | taskEVENT_LIST_ITEM_VALUE_IN_USE );

	/* Remove the event list form the event flag.  Interrupts do not access
	event flags. */
	pxUnblockedTCB = listGET_LIST_ITEM_OWNER( pxEventListItem ); /*lint !e9079 void * is used as this macro is used with timers and co-routines too.  Alignment is known to be fine as the type of the pointer stored and retrieved is the same. */
	configASSERT( pxUnblockedTCB );

	( void ) uxListRemove( pxEventListItem );

	/* Remove the task from the delayed list and add it to the ready list.  The
	scheduler is suspended so interrupts will not be accessing the ready
	lists. */
	( void ) uxListRemove( &( pxUnblockedTCB->xStateListItem ) );
	prvAddTaskToReadyList( pxUnblockedTCB, 1);

	if( pxUnblockedTCB->uxPriority > pxCurrentTCB[pxUnblockedTCB->bind_cpu]->uxPriority )
	{
		/*SMP_DBG("acpu %d. cpu %d. pri1 %d, pri %d.\n", processor_id, \*/
		/*pxUnblockedTCB->bind_cpu, pxUnblockedTCB->uxPriority, pxCurrentTCB[pxUnblockedTCB->bind_cpu]->uxPriority);*/
		/* The unblocked task has a priority above that of the calling task, so
		a context switch is required.  This function is called with the
		scheduler suspended so xYieldPending[processor_id] is set so the context switch
		occurs immediately that the scheduler is resumed (unsuspended). */
		xYieldPending[pxUnblockedTCB->bind_cpu] = pdTRUE;

		if(pxUnblockedTCB->bind_cpu != processor_id)
		{
			xYieldPending[pxUnblockedTCB->bind_cpu] = pdFALSE;
			send_ipi_interrupt(IPI_SCHEDU, 1 << pxUnblockedTCB->bind_cpu, 0, 1);
		}
	}
	taskEXIT_CRITICAL(cpu_sr);
}
