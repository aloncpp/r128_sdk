#ifndef __DMA_SUN55IW3_H__
#define __DMA_SUN55IW3_H__

#if defined(CONFIG_ARCH_RISCV_E906)
#define WDT_BASE                0x07132000
#elif defined(CONFIG_CORE_DSP0)
#define WDT_BASE                0x07100400
#endif
/* watchdog register offset */
#define WDT_IRQ_EN              (WDT_BASE + 0x00)
#define WDT_STA                 (WDT_BASE + 0x04)
#define WDT_CTL                 (WDT_BASE + 0x0C)
#define WDT_CFG                 (WDT_BASE + 0x10)
#define WDT_MODE                (WDT_BASE + 0x14)
#define WDT_OUT_CFG             (WDT_BASE + 0x18)


#define WDT_CTRL_RESTART        (0x1 << 0)
#define WDT_CTRL_KEY            (0x0a57 << 1)

#define WDT_CFG_RESET           (0x1)
#define WDT_MODE_EN             (0x1)

struct hal_sunxi_wdt {
        volatile u32 irq_en;    //0x00
        volatile u32 sta;       //0x04
        volatile u32 rsl[1];
        volatile u32 ctl;       //0x0C
        volatile u32 cfg;       //0x10
        volatile u32 mode;      //0x14
};

#endif /*__DMA_SUN55IW3_H__  */
